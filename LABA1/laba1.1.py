L = float(input("Введите длину бревна L: "))
x = float(input("Введите длину куска x: "))

if x <= 0:
    print("Ошибка: длина куска должна быть положительной")
elif L <= 0:
    print("Ошибка: длина бревна должна быть положительной")
elif x > L:
    print("Кусок длиннее бревна, невозможно отпилить ни одного куска")
else:
    # Вычисление количества целых кусков
    count = L // x

    print(f"Максимальное количество целых полноразмерных кусков: {count}")

    def calculate_pieces():
        try:

            # Ввод данных
            L = float(input("Введите длину бревна L: "))
            x = float(input("Введите длину куска x: "))

            # Проверка корректности данных
            if L <= 0 or x <= 0:
                return "Ошибка: длины должны быть положительными числами"
            if x > L:
                return "Невозможно отпилить ни одного куска - кусок длиннее бревна"

            # Вычисление результата
            pieces = L // x
            remainder = round(L % x)

            return f"Можно отпилить {pieces} целых кусков. Останется {remainder} единиц длины."
        except ValueError:
            return "Ошибка: введите целые числа"

    # Запуск программы
    print(calculate_pieces())