# Функция проверки попадания точки в квадрат
def is_point_in_red_square(x, y):
    # Проверяем, что координата X находится между 1 и 3 (включая границы)
    # Это левая и правая красные границы квадрата
    x_in_range = (1 <= x <= 3)

    # Проверяем, что координата Y находится между -2 и -1 (включая границы)
    # Это нижняя и верхняя красные границы квадрата
    y_in_range = (-2 <= y <= -1)

    # Точка находится в квадрате, если удовлетворяет ОБА условия:
    # находится в диапазоне по X И в диапазоне по Y
    return x_in_range and y_in_range


# Запрашиваем координаты у пользователя
x = float(input("Введите координату x: "))  # Преобразуем ввод в число с плавающей точкой
y = float(input("Введите координату y: "))  # Преобразуем ввод в число с плавающей точкой

# Проверяем, находится ли точка в квадрате
if is_point_in_red_square(x, y):
    print(f"Точка ({x}, {y}) находится в квадрате")  # Точка в нужной области
else:
    print(f"Точка ({x}, {y}) НЕ находится в квадрате")  # Точка вне квадрата