def count_ones(n):
    """
    Рекурсивная функция для подсчёта количества единиц
    в двоичной записи числа n.
    """
    # Базовый случай: когда число равно 0
    if n == 0:
        return 0

    # Рекурсивный случай:
    # Последний бит = n % 2
    # Остальная часть числа = n // 2
    return (n % 2) + count_ones(n // 2)


# Основная программа
num = int(input("Введите целое число: "))

# Для отрицательных чисел возьмём модуль, чтобы считать биты только значения
ones = count_ones(abs(num))

print(f"Количество единиц в двоичной записи числа {num}: {ones}")