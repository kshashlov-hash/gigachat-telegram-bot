# ФУНКЦИЯ ПРОВЕРКИ МАТА
# ------------------------------------------------------------
def contains_bad_words(text: str) -> bool:
    """Проверяет, есть ли мат в тексте"""
    if not text:
        return False
    text_lower = text.lower()
    for word in BAD_WORDS:
        if word in text_lower:
            return True
    return False

# ------------------------------------------------------------
