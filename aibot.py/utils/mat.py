# ЕДИНСТВЕННАЯ ФУНКЦИЯ — ВОЗВРАЩАЕТ ПСЕВДО-МАТ
# ------------------------------------------------------------
def get_swear(probability: float = 0.29, add_emoji: bool = True) -> str:
    """
    Возвращает псевдо-мат с заданной вероятностью.

    probability: от 0 до 1 (0.05 = 5% шанс получить непустую строку)
    add_emoji: добавить случайный эмодзи в конце

    Пример использования:
        swear = get_swear(0.1)
        if swear:
            ответ = f"{swear} Ну ты спросил, конечно!"
    """
    if random.random() > probability:
        return ""

    swear = random.choice(PSEUDO_SWEARS)
    if add_emoji:
        swear += " " + random.choice(EMOJI)

    return swear